package com.cutepet.domain.User;

public class User {

    private long id;
    private String name;
    private String phone;
    private String address;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public String getAddress() {
        return address;
    }
}
